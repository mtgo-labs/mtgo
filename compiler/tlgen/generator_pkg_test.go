package tlgen

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func TestGeneratePackageFiles(t *testing.T) {
	tmpDir := t.TempDir()
	err := GeneratePackageFiles(tmpDir, "types", 224)
	if err != nil {
		t.Fatal(err)
	}

	data, err := os.ReadFile(filepath.Join(tmpDir, "doc.go"))
	if err != nil {
		t.Fatal(err)
	}
	content := string(data)
	if !strings.Contains(content, "Code generated by tlgen") {
		t.Fatal("missing generated header")
	}

	layerData, err := os.ReadFile(filepath.Join(tmpDir, "layer_gen.go"))
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(string(layerData), "const Layer = 224") {
		t.Fatal("missing generated layer constant")
	}
}

func TestGeneratePackageFilesRejectsInvalidLayer(t *testing.T) {
	if err := GeneratePackageFiles(t.TempDir(), "types", 0); err == nil {
		t.Fatal("expected invalid layer error")
	}
}

func TestWriteGoFilePreservesExistingFileOnFormatError(t *testing.T) {
	path := filepath.Join(t.TempDir(), "output.go")
	if err := os.WriteFile(path, []byte("original"), 0o644); err != nil {
		t.Fatal(err)
	}
	if err := writeGoFile(path, "package broken {"); err == nil {
		t.Fatal("expected format error")
	}
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatal(err)
	}
	if string(data) != "original" {
		t.Fatalf("existing file changed after failed generation: %q", data)
	}
}

