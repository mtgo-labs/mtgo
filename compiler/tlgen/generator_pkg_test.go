package tlgen

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func TestGeneratePackageFiles(t *testing.T) {
	tmpDir := t.TempDir()
	err := GeneratePackageFiles(tmpDir, "types", 224)
	if err != nil {
		t.Fatal(err)
	}

	data, err := os.ReadFile(filepath.Join(tmpDir, "doc.go"))
	if err != nil {
		t.Fatal(err)
	}
	content := string(data)
	if !strings.Contains(content, "Code generated by tlgen") {
		t.Fatal("missing generated header")
	}
}
