// Package mtgo is a Go client library for the Telegram MTProto API.
//
// mtgo provides a full-featured implementation of MTProto 2.0, the protocol
// used by Telegram clients. It supports session management, transport layer
// abstraction (TCP, WebSocket, MTProxy), TL schema compilation, and a rich
// set of Telegram API helpers.
//
// Key features:
//   - Full MTProto 2.0 implementation with encryption and key exchange
//   - Session management with multiple storage backends
//   - TL schema compiler and code generator
//   - Built-in support for MTProxy (obfuscated transport)
//   - Comprehensive Telegram type system
package mtgo
