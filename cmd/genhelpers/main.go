// Command genhelpers generates generic wrapper functions for all exported
// *Client methods that accept chatID int64 or userID int64 parameters.
//
// The wrappers accept int | int64 | string as the identifier type (constrained
// by generic.PeerInput) and resolve string identifiers to int64 via the
// Client's peer resolver before delegating to the original method. The first
// argument accepts both *telegram.Client and *telegram.Context (constrained by
// generic.Caller); the *telegram.Client and context.Context are extracted
// internally via extractClient.
//
// Usage:
//
//	go run ./cmd/genhelpers
//
// The output file is telegram/generic/gen_helpers.go. DO NOT EDIT manually.
package main

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/printer"
	"go/token"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

const outputHeader = `// Code generated by genhelpers; DO NOT EDIT.
// To regenerate run: go run ./cmd/genhelpers

package generic

import (
	"github.com/mtgo-labs/mtgo/telegram"
	"github.com/mtgo-labs/mtgo/telegram/params"
	"github.com/mtgo-labs/mtgo/telegram/types"
	"github.com/mtgo-labs/mtgo/tg"
)

`

type paramInfo struct {
	name     string
	typeStr  string
	variadic bool
}

type methodInfo struct {
	name     string
	params   []paramInfo
	results  []string
	wrapName string // wrapper-local name ("chat" or "user")
	origName string // original param name ("chatID" or "userID")
	wrapIdx  int    // index into params
}

func main() {
	telegramDir := "telegram"
	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, telegramDir, func(fi os.FileInfo) bool {
		return strings.HasSuffix(fi.Name(), ".go") && !strings.HasSuffix(fi.Name(), "_test.go")
	}, parser.ParseComments)
	if err != nil {
		fatal("parse telegram/: %v", err)
	}

	var methods []methodInfo
	for _, pkg := range pkgs {
		for _, file := range pkg.Files {
			for _, decl := range file.Decls {
				fn, ok := decl.(*ast.FuncDecl)
				if !ok || fn.Recv == nil || len(fn.Recv.List) == 0 {
					continue
				}
				if !isClientReceiver(fn.Recv.List[0]) {
					continue
				}
				if !ast.IsExported(fn.Name.Name) {
					continue
				}
				mi, ok := analyzeMethod(fn, fset)
				if !ok {
					continue
				}
				methods = append(methods, mi)
			}
		}
	}

	sort.Slice(methods, func(i, j int) bool {
		return methods[i].name < methods[j].name
	})

	var buf bytes.Buffer
	buf.WriteString(outputHeader)

	for _, m := range methods {
		generateWrapper(&buf, m)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fatal("format output: %v\n--- raw output ---\n%s", err, buf.String())
	}

	outPath := filepath.Join("telegram", "generic", "gen_helpers.go")
	if err := os.WriteFile(outPath, formatted, 0o644); err != nil {
		fatal("write %s: %v", outPath, err)
	}

	fmt.Printf("Generated %d wrappers → %s\n", len(methods), outPath)
}

func isClientReceiver(field *ast.Field) bool {
	star, ok := field.Type.(*ast.StarExpr)
	if !ok {
		return false
	}
	ident, ok := star.X.(*ast.Ident)
	return ok && ident.Name == "Client"
}

func analyzeMethod(fn *ast.FuncDecl, fset *token.FileSet) (methodInfo, bool) {
	params := splitFieldList(fn.Type.Params, fset)
	if len(params) == 0 {
		return methodInfo{}, false
	}
	if params[0].name != "ctx" || params[0].typeStr != "context.Context" {
		return methodInfo{}, false
	}

	mi := methodInfo{params: params}

	for i, p := range params {
		if p.name == "chatID" && p.typeStr == "int64" {
			mi.wrapIdx = i
			mi.wrapName = "chat"
			mi.origName = "chatID"
			break
		}
	}
	if mi.wrapName == "" {
		for i, p := range params {
			if p.name == "userID" && p.typeStr == "int64" {
				mi.wrapIdx = i
				mi.wrapName = "user"
				mi.origName = "userID"
				break
			}
		}
	}
	if mi.wrapName == "" {
		return methodInfo{}, false
	}

	mi.name = fn.Name.Name
	if fn.Type.Results != nil {
		mi.results = splitResultTypes(fn.Type.Results, fset)
	}
	return mi, true
}

func splitFieldList(fl *ast.FieldList, fset *token.FileSet) []paramInfo {
	if fl == nil {
		return nil
	}
	var out []paramInfo
	for _, field := range fl.List {
		typeStr := exprString(field.Type, fset)
		variadic := false
		if ell, ok := field.Type.(*ast.Ellipsis); ok {
			variadic = true
			typeStr = "[]" + exprString(ell.Elt, fset)
		}
		names := field.Names
		if len(names) == 0 {
			out = append(out, paramInfo{name: "", typeStr: typeStr, variadic: variadic})
			continue
		}
		for _, n := range names {
			out = append(out, paramInfo{name: n.Name, typeStr: typeStr, variadic: variadic})
		}
	}
	return out
}

// splitResultTypes extracts the result type strings, handling unnamed results.
func splitResultTypes(fl *ast.FieldList, fset *token.FileSet) []string {
	if fl == nil {
		return nil
	}
	var out []string
	for _, field := range fl.List {
		typeStr := exprString(field.Type, fset)
		if len(field.Names) == 0 {
			out = append(out, typeStr)
			continue
		}
		for range field.Names {
			out = append(out, typeStr)
		}
	}
	return out
}

func generateWrapper(buf *bytes.Buffer, m methodInfo) {
	// Signature: func Name[T PeerInput, C Caller](c C, <wrapped params, skip ctx>)
	buf.WriteString("func ")
	buf.WriteString(m.name)
	buf.WriteString("[T PeerInput, C Caller](c C")

	for i, p := range m.params {
		if i == 0 {
			continue // skip ctx context.Context — extracted via extractClient
		}
		buf.WriteString(", ")
		if i == m.wrapIdx {
			buf.WriteString(m.wrapName)
			buf.WriteString(" T")
			continue
		}
		buf.WriteString(p.name)
		buf.WriteString(" ")
		if p.variadic {
			buf.WriteString("...")
			buf.WriteString(qualifyType(strings.TrimPrefix(p.typeStr, "[]")))
		} else {
			buf.WriteString(qualifyType(p.typeStr))
		}
	}
	buf.WriteString(")")

	// Results
	if len(m.results) > 0 {
		qualified := make([]string, len(m.results))
		for i, r := range m.results {
			qualified[i] = qualifyType(r)
		}
		if len(qualified) == 1 {
			buf.WriteString(" " + qualified[0])
		} else {
			buf.WriteString(" (" + strings.Join(qualified, ", ") + ")")
		}
	}
	buf.WriteString(" {\n")

	// Body: extract client + ctx from the Caller, then resolve the generic input
	buf.WriteString("\tclient, ctx := extractClient(c)\n")
	fmt.Fprintf(buf, "\t%s, err := resolveID[T](ctx, client, %s)\n", m.origName, m.wrapName)
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString("\t\treturn")
	for i, r := range m.results {
		if i > 0 {
			buf.WriteString(",")
		}
		buf.WriteString(" " + zeroValue(r))
	}
	buf.WriteString("\n")
	buf.WriteString("\t}\n")

	// Delegation
	buf.WriteString("\treturn client.")
	buf.WriteString(m.name)
	buf.WriteString("(")
	for i, p := range m.params {
		if i > 0 {
			buf.WriteString(", ")
		}
		if p.variadic {
			buf.WriteString(p.name + "...")
		} else {
			buf.WriteString(p.name)
		}
	}
	buf.WriteString(")\n")

	buf.WriteString("}\n\n")
}

func zeroValue(s string) string {
	if s == "error" {
		return "err"
	}
	if strings.HasPrefix(s, "*") || strings.HasPrefix(s, "[]") || strings.HasPrefix(s, "map[") || strings.HasPrefix(s, "chan ") {
		return "nil"
	}
	switch s {
	case "int", "int32", "int64", "uint", "uint32", "uint64",
		"float32", "float64":
		return "0"
	case "bool":
		return "false"
	case "string":
		return `""`
	}
	return "nil"
}

// qualifyType prefixes unqualified non-builtin types with "telegram." so they
// resolve correctly in the generic package's import context.
func qualifyType(s string) string {
	if strings.HasPrefix(s, "*") {
		return "*" + qualifyType(strings.TrimPrefix(s, "*"))
	}
	if strings.HasPrefix(s, "[]") {
		return "[]" + qualifyType(strings.TrimPrefix(s, "[]"))
	}
	if strings.Contains(s, ".") || isBuiltin(s) {
		return s
	}
	return "telegram." + s
}

func isBuiltin(s string) bool {
	switch s {
	case "int", "int8", "int16", "int32", "int64",
		"uint", "uint8", "uint16", "uint32", "uint64",
		"float32", "float64", "string", "bool",
		"byte", "rune", "error", "any":
		return true
	}
	return false
}

func exprString(expr ast.Expr, fset *token.FileSet) string {
	if fset == nil {
		fset = token.NewFileSet()
	}
	var buf bytes.Buffer
	printer.Fprint(&buf, fset, expr)
	return buf.String()
}

func fatal(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "genhelpers: "+format+"\n", args...)
	os.Exit(1)
}
