package main

import (
	"bufio"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
)

type errorDef struct {
	GoName  string
	Const   string
	IsFunc  string
	Comment string
}

func main() {
	if len(os.Args) != 3 {
		fmt.Fprintf(os.Stderr, "usage: errgen <source-dir> <output-file>\n")
		os.Exit(1)
	}

	sourceDir := os.Args[1]
	outputFile := os.Args[2]

	files, err := filepath.Glob(filepath.Join(sourceDir, "*.tsv"))
	if err != nil {
		fmt.Fprintf(os.Stderr, "glob error: %v\n", err)
		os.Exit(1)
	}

	seen := make(map[string]bool)
	var defs []errorDef

	for _, f := range files {
		defs = append(defs, parseTSV(f, seen)...)
	}

	sort.Slice(defs, func(i, j int) bool {
		return defs[i].GoName < defs[j].GoName
	})

	var b strings.Builder
	b.WriteString("// Code generated by cmd/errgen. DO NOT EDIT.\n\n")
	b.WriteString("package tgerr\n\n")
	b.WriteString("// Telegram API error types.\nconst (\n")
	for _, d := range defs {
		fmt.Fprintf(&b, "\t%s = %q\n", d.GoName, d.Const)
	}
	b.WriteString(")\n\n")
	for _, d := range defs {
		fmt.Fprintf(&b, "// %s reports whether err is %s.\n", d.IsFunc, d.Const)
		fmt.Fprintf(&b, "func %s(err error) bool {\n", d.IsFunc)
		fmt.Fprintf(&b, "\treturn Is(err, %s)\n", d.GoName)
		fmt.Fprintf(&b, "}\n\n")
	}

	if err := os.WriteFile(outputFile, []byte(b.String()), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "write error: %v\n", err)
		os.Exit(1)
	}

	fmt.Printf("Generated %d error constants to %s\n", len(defs), outputFile)
}

var validID = regexp.MustCompile(`^[A-Z0-9][A-Z0-9_]*$`)

func parseTSV(path string, seen map[string]bool) []errorDef {
	f, err := os.Open(path)
	if err != nil {
		fmt.Fprintf(os.Stderr, "open %s: %v\n", path, err)
		os.Exit(1)
	}
	defer f.Close()

	var defs []errorDef
	scanner := bufio.NewScanner(f)
	first := true

	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if first {
			first = false
			continue
		}
		if line == "" {
			continue
		}

		parts := strings.SplitN(line, "\t", 2)
		id := strings.TrimSpace(parts[0])

		if id == "" || !validID.MatchString(id) {
			continue
		}

		constValue := id
		if strings.HasSuffix(id, "_X") {
			constValue = id[:len(id)-2]
		}

		if seen[constValue] {
			continue
		}
		seen[constValue] = true

		pascal := toPascalCase(constValue)
		goName := "Err" + pascal
		isFunc := "Is" + pascal

		// Deduplicate by Go name: different constant strings (e.g.
		// GROUP_CALL_INVALID vs GROUPCALL_INVALID) can collapse to the same
		// identifier after PascalCase conversion. Keep the first occurrence.
		if seen[goName] {
			continue
		}
		seen[goName] = true

		defs = append(defs, errorDef{
			GoName: goName,
			Const:  constValue,
			IsFunc: isFunc,
		})
	}

	if err := scanner.Err(); err != nil {
		fmt.Fprintf(os.Stderr, "scan %s: %v\n", path, err)
		os.Exit(1)
	}

	return defs
}

func toPascalCase(s string) string {
	parts := strings.Split(s, "_")
	var b strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		b.WriteString(titlePart(p))
	}
	return b.String()
}

// pascalOverrides maps TSV tokens to their exact PascalCase rendering.
// Pure acronyms (ID, API, URL) are uppercased as-is per golint convention.
// Compound tokens (MSGID, GROUPCALL) that merge words without an underscore
// get an explicit mixed-case form, since titlePart() can't split them.
var pascalOverrides = map[string]string{
	// Pure acronyms — returned as-is.
	"2FA": "2FA", "API": "API", "CDN": "CDN", "DC": "DC", "DH": "DH",
	"ID": "ID", "IDS": "IDS", "JSON": "JSON", "MD5": "MD5", "MIME": "MIME",
	"MSG": "MSG", "MT": "MT", "NA": "NA", "PNG": "PNG", "PTS": "PTS",
	"QTS": "QTS", "RPC": "RPC", "RSA": "RSA", "SHA256": "SHA256",
	"SMS": "SMS", "SRP": "SRP", "SSRC": "SSRC", "TGS": "TGS",
	"TTL": "TTL", "URL": "URL",
	// Compound tokens — explicit split (matches gotd TL naming).
	"CHATLINK": "ChatLink", "CHATLINKS": "ChatLinks",
	"CHATLIST": "ChatList", "CHATLISTS": "ChatLists",
	"FILEREF": "FileRef", "GROUPCALL": "GroupCall",
	"MSGID": "MsgID", "STARGIFT": "StarGift", "STARGIFTS": "StarGifts",
	"STARREF": "StarRef", "STICKERPACK": "StickerPack",
	"STICKERSET": "StickerSet", "WEBDOCUMENT": "WebDocument",
	"WEBPAGE": "WebPage", "WEBPUSH": "WebPush", "WEBVIEW": "WebView",
}

func titlePart(s string) string {
	if v, ok := pascalOverrides[s]; ok {
		return v
	}
	if len(s) == 0 {
		return s
	}
	return strings.ToUpper(s[:1]) + strings.ToLower(s[1:])
}
